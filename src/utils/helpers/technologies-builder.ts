export const parseTechnologies = (techString: string) => {
  return techString.split(";").map((tech) => tech.trim());
};

export const getRandomColor = () => {
  const letters = "0123456789ABCDEF";
  let color = "#";
  for (let i = 0; i < 6; i++) {
    color += letters[Math.floor(Math.random() * 16)];
  }
  return color;
};
